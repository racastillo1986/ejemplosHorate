package Modelo;

public class Personas {

    private int id_click;
    private String ced_per;
    private String nom_per;
    private String mail_per;
    private String tel_per;
    private String edad_per;
    private String pue_click;

    public int getId_click() {
        return id_click;
    }

    public void setId_click(int id_click) {
        this.id_click = id_click;
    }

   

    public String getCed_per() {
        return ced_per;
    }

    public void setCed_per(String ced_per) {
        this.ced_per = ced_per;
    }

    public String getNom_per() {
        return nom_per;
    }

    public void setNom_per(String nom_per) {
        this.nom_per = nom_per;
    }

    public String getMail_per() {
        return mail_per;
    }

    public void setMail_per(String mail_per) {
        this.mail_per = mail_per;
    }

    public String getTel_per() {
        return tel_per;
    }

    public void setTel_per(String tel_per) {
        this.tel_per = tel_per;
    }

    public String getEdad_per() {
        return edad_per;
    }

    public void setEdad_per(String edad_per) {
        this.edad_per = edad_per;
    }

    public String getPue_click() {
        return pue_click;
    }

    public void setPue_click(String pue_click) {
        this.pue_click = pue_click;
    }

}
