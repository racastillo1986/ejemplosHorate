
package Modelo;

public class Cita {
    
    private int id_cit;
    private int id_pac;
    private String fec_cit;
    private String mot_cit;
    private String cau_cit;

    public int getId_cit() {
        return id_cit;
    }

    public void setId_cit(int id_cit) {
        this.id_cit = id_cit;
    }

    public int getId_pac() {
        return id_pac;
    }

    public void setId_pac(int id_pac) {
        this.id_pac = id_pac;
    }

    public String getFec_cit() {
        return fec_cit;
    }

    public void setFec_cit(String fec_cit) {
        this.fec_cit = fec_cit;
    }

    public String getMot_cit() {
        return mot_cit;
    }

    public void setMot_cit(String mot_cit) {
        this.mot_cit = mot_cit;
    }

    public String getCau_cit() {
        return cau_cit;
    }

    public void setCau_cit(String cau_cit) {
        this.cau_cit = cau_cit;
    }
    
    
    
    
    
}
